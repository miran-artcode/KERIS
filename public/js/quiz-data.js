export function quizForSlide(deck, slide) {
  return QUIZ_LIST.find(q => q.deck === deck && q.slide === slide) || null;
}

/* 점수: 맞히기 게임은 빠를수록 높게(60~100), 오답 10점. 투표는 참여 30점 */
export function calcPoints(quiz, answerIdx, elapsedMs) {
  if (quiz.type !== 'score') return 30;
  if (answerIdx !== quiz.ans) return 10;
  const ratio = Math.max(0, 1 - elapsedMs / (quiz.time * 1000));
  return 60 + Math.round(40 * ratio);
}

